// do...while statement
let i = 0
do {
  i += 1
  console.log(i)
} while (i < 5)

// Kode di atas membuat variabel i dan memberikan nilai awal 0.
// Kemudian masuk ke dalam perulangan do-while.
// Perulangan ini mirip dengan perulangan while,
// tetapi blok kode di dalam perulangan dieksekusi setidaknya satu kali sebelum kondisi diperiksa.
// Dalam perulangan, variabel i ditambah 1 dan nilai saat ini dari i dicetak ke console menggunakan fungsi console.log().
// Perulangan akan terus dijalankan selama nilai i kurang dari 5.
// Jadi output yang akan dihasilkan adalah 1 2 3 4 5.
