// break statement
// dengan for
for (var i = 0; i < 20; i++) {
  if (i === 10) {
    break
  }
  console.log(i)
}

// Dalam contoh di atas, perulangan "for" akan dihentikan saat i mencapai 10
// dan tidak akan mengeksekusi perintah di dalam perulangan untuk i yang lebih besar dari 10.

